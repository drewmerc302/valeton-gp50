def crc8(data: bytes) -> int:
    crc = 0
    for b in data:
        crc ^= b
        for _ in range(8):
            crc = ((crc << 1) ^ 0x07) & 0xFF if crc & 0x80 else (crc << 1) & 0xFF
    return crc


def denib(nibs: list[int]) -> bytes:
    """Pair nibbles hi-first back into bytes."""
    return bytes((nibs[i] << 4) | nibs[i + 1] for i in range(0, len(nibs), 2))


def decode(line: str):
    b = [int(x, 16) for x in line.split()]
    assert b[0] == 0xF0 and b[-1] == 0xF7
    buf = denib(b[1:-1])  # [crc, cmd, index, length, *payload]
    return buf


def reassemble(lines) -> bytes:
    """Concatenate the payloads of a list of 0x1D write packets, in order."""
    out = bytearray()
    for line in lines:
        buf = decode(line)
        out += bytes(buf[4 : 4 + buf[3]])
    return out


